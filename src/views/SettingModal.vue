<template>
  <transition name="modal" appear>
    <div class="modal modal-overlay" @click.self="$emit('close')">
      <div class="modal-window">
        <div class="modal-content">
          <h2>Settings</h2>
          <table>
            <tbody>
              <tr>
                <th>タイマー間隔<br /><span>(最小: 5000msec)</span></th>
                <td>
                  <input
                    type="number"
                    min="5000"
                    v-model="timer"
                    step="100"
                  />msec
                </td>
              </tr>
              <tr>
                <th>プレイ中人数</th>
                <td><input type="number" min="1" v-model="playing" />人</td>
              </tr>
              <tr>
                <th>待ち表示上限</th>
                <td><input type="number" min="0" v-model="standby" />人</td>
              </tr>
              <tr>
                <th>
                  自動予約キーワード<br />
                  <span
                    >(正規表現・空欄だと機能停止・大文字小文字の区別無し)</span
                  >
                </th>
                <td><textarea v-model="regex"></textarea></td>
              </tr>
            </tbody>
          </table>
        </div>
        <footer class="modal-footer">
          <slot name="footer">
            <button @click="$emit('close')">閉じる</button>
          </slot>
        </footer>
      </div>
    </div>
  </transition>
</template>

<script>
import { mapActions, mapState } from "vuex";

export default {
  name: "SettingModal",
  methods: {
    ...mapActions([
      "setTimerInterval",
      "setNumberOfPlaying",
      "setNumberOfStandby",
      "setReserveKeyword"
    ])
  },
  computed: {
    ...mapState([
      "timerInterval",
      "numberOfPlaying",
      "numberOfStandby",
      "reserveKeyword"
    ]),
    timer: {
      get() {
        return this.timerInterval;
      },
      set(val) {
        this.setTimerInterval(val);
      }
    },
    playing: {
      get() {
        return this.numberOfPlaying;
      },
      set(val) {
        this.setNumberOfPlaying(val);
      }
    },
    standby: {
      get() {
        return this.numberOfStandby;
      },
      set(val) {
        this.setNumberOfStandby(val);
      }
    },
    regex: {
      get() {
        return this.reserveKeyword;
      },
      set(val) {
        this.setReserveKeyword(val);
      }
    }
  }
};
</script>
